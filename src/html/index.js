function renderChart3 () {
  let data = this.chart3Data
  let x_data = []
  let series = [{
    name: '数量',
    type:'bar',
    barWidth: 8,
    z: 1,
    data: []
  },{
    name: '数量',
    type:'pictorialBar',
    symbol: 'rect',
    symbolRepeat: true,
    symbolSize: ['40%', 3],
    symbolMargin: 1,
    barWidth: 25,
    z: 2,
    data: []
  }]
  let y_max = 0
  data.forEach((el, i) => {
    if (el.value > y_max) {
      y_max = el.value
    }
    x_data.push(el.name)
    series[1].data.push({value: Number(el.value), name: el.name,
      itemStyle: {
        normal:{
          show: true,
          // 柱子的颜色
          color: i % 2 === 0 ? '#FF4F11' : '#F39800'
        }
      }
    })
  })
  data.forEach((el, i) => {
    series[0].data.push({value: Number(y_max * 1.1), name: el.name,
      itemStyle: {
        normal:{
          show: true,
          // 柱子的背景色
          color: i % 2 === 0 ? '#FEE4E4' : '#FEF6E4'
        }
      }
    })
  })
  let tooltip = {
    trigger: "axis",
    axisPointer: {
      type: "shadow"
    },
    formatter: '{b}<br/>数量：{c1}'
  }
  // 表格id, 标题, 系列名称, x轴数据, 系列数据, series, tooltip, y轴的最大值, x轴名, y轴名
  this.chart3 = this.$chart.bar("chart3", "", "", x_data, null, series, tooltip, Math.round(y_max * 1.1), '日期', '人数')
}
