function renderChart2 () {
  let data = this.chart2Data
  let series = []
  let legend_data = []
  let x_data = data[0].data.map((el, i) => {
    return el.name
  })
  let color = ['rgba(116, 240, 24, 1)', 'rgba(254, 74, 74, 1)', 'rgba(91, 130, 252, 1)', 'rgba(255, 159, 35, 1)', 'rgba(255, 57, 57, 1)', 'rgba(255, 10, 206, 1)', 'rgba(171, 7, 239, 1)', 'rgba(17, 17, 222, 1)', 'rgba(16, 247, 255, 1)', 'rgba(252, 255, 35, 1)']
  data.forEach((el, i) => {
    let obj = {
      name: el.name,
      type: "line", 
      data: [],
      itemStyle: {
        // 折线的颜色
        color: color[i]
      },
      // 区域阴影 不需要去掉即可
      areaStyle: {
        color: {
          type: 'linear',
          x: 0,
          y: 0,
          x2: 0,
          y2: 1,
          colorStops: [{
              offset: 0, color: color[i].slice(0, -3) + ' 0.1)' // 0% 处的颜色
          }, {
              offset: 1, color: color[i].slice(0, -3) + ' 0)' // 100% 处的颜色
          }]
        }
      }
    }
    obj.data = el.data.map((v, j) => {
      return {name: v.name, value: v.value}
    })
    series.push(obj)
    legend_data.push(el.name)
  })
  let dataZoom = [
    {
      id: 'dataZoomX',
      show: true,
      type: 'slider',
      height: 8,
      handleSize: 8,
      cursor: 'pointer',
      start: 0,
      end: 100,
      xAxisIndex: 0,
      // 滚动条颜色
      fillerColor: '#0fc01c',   
      backgroundColor: '#e8e8e8',
      filterColor: '#ff7b01',
      borderColor: '#e8e8e8',
      bottom: '0'
    },
    {
      type: 'inside',
      xAxisIndex: 0,
      minSpan:1
    }
  ]
  // 表格id, title, x轴数据, 每条折线的数据, 图例数据, tooltip， dataZoom，表格下边距
  this.chart2 = this.$chart.line("chart2", "", x_data, series, legend_data, null, dataZoom, '5%')
}
