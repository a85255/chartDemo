// X_data2放入的是背景底色柱的数据，他的所有值都是X_data1中的最大值的1.1倍（倍数可自己修改）
function renderChart1 () {
  console.log(this)
  let data = this.chart1Data.sort((a, b) => {
    return a.value - b.value
  })
  let y_data = []
  let x_data1 = []
  let x_data2 = []
  data.forEach((el, i) => {
    y_data.push(el.name)
    x_data1.push(
      {
        value: Math.round(el.value, 0), 
        name: el.name, 
        label: el.name,
        itemStyle: {
          normal:{
            show: true,
            barBorderRadius: 100,
            //  柱子的颜色
            color: i % 2 ? '#FF7B01' : '#FFBA00'
          }
        }
      }
    )
    x_data2.push(
      {
        value: Math.round(data[data.length - 1].value * 1.1),
        name: data.length - i, 
        label: data.length - i,
        itemStyle: {
          normal:{
            show: true,
            barBorderRadius: 100,
            // 柱子的背景色
            color: '#F3FAFF'
          }
        }
      }
    )
  })
  let tooltip = {
    axisPointer: {
      type: 'shadow'
    },
    trigger: 'axis',
    formatter: '排名：{b0}'
  }
  // 表格id, y轴的数据, 滚动条的颜色, tooltip, x轴的数据, 柱状图的底色, 是否显示底色, 滚动条的起始范围
  this.chart1 = this.$chart.bar_scroll('chart1', y_data, '#FF7B01', tooltip, x_data1, x_data2, true, [(x_data1.length - 12) / x_data1.length * 100,100])
}
