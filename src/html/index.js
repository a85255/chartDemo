// legend.data放入数据的名称
function renderChart6 () {
  let data = this.chart6Data
  this.chart6 = echarts.init(document.getElementById('chart6'))
  this.chart6.clear()
  let total = 0
  data.forEach(el => {
    total += el.value
  })
  let color = ['#15D7F4', '#5B82FC', '#FF4F11', '#FF7B01']
  let legend = {
    top: 80,
    right: '5%',
    width: 320,
    textStyle: {
      fontWeight: 'bold',
      fontSize: 16,
      lineHeight: 24,
      padding: [0, 50, 0, 10]
    },
    data: [],
  }
  let series_data = []
  data.forEach((el, i) => {
    let name = el.name + '\n' + (el.value / total * 100).toFixed(2) + '%'
    series_data.push({name: name, value: el.value})
    legend.data.push({name: name, textStyle: {color: color[i]}})
  })
  let series = {
    type: "pie",
    radius: ["55%", "75%"],
    startAngle: 45,
    label: {
      show: false
    },
    lableLine: {
      show: false
    },
    itemStyle: {
      borderColor: '#fff',
      borderWidth: 6
    },
    center: ['25%', '50%'],
    data: series_data
  }
  let options = {
    legend: legend,
    color: color,
    series: series
  }
  this.chart6.setOption(options)
}
